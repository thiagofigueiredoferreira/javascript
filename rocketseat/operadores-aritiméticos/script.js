
// Operadores Aritimédicos

/*
  console.log(...)
Mostra o valor no console do navegador ou terminal.
Você está usando uma string explicativa antes do valor para deixar a saída mais legível.
*/ 

console.log("SOMA: ", 12 + 8)
// + entre números faz adição, Resultado: 20.


console.log("CONCATENAÇÃO: ","12" + "8")
// + entre strings concatena, ou seja, une os textos. Resultado: "128".

console.log("SUBTRAÇÃO:", 12 - 10)
//- só faz sentido para números. Resultado: 2.

console.log("MULTIPLICAÇÃO ", 3 * 5.5)
/* multiplica. Resultado: 16.5.*/

console.log("DIVISÃO ", 12 / 2)
/* divide. Resultado: 6*/

console.log("RESTO DA DIVISÃO: ", 13 % 2)
// % retorna o resto da divisão inteira. Aqui, 13 dividido por 2 dá resto 1.

console.log("EXPONENCIAL: ", 3 ** 3)
/* eleva um número à potência. 3 ** 3 significa 3 * 3 * 3, ou seja, 27.*/

/* Fundamentos importantes
JavaScript tem um único tipo numérico padrão: number.
Operadores aritméticos funcionam com valores numéricos.
O operador + é especial: ele soma números, mas concatena strings.
Se usar strings com +, o JavaScript junta os textos.
console.log é útil para testar e ver resultados imediatamente.
Esse código é básico, mas muito importante para entender como fazer cálculos e exibir resultados em JavaScript.
*/