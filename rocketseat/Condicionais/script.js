// Operador condicional ternário

let age = 21
console.log(
    age >= 18 // condição
     ? "Você pode dirigir" // se verdadeira
    :"Você não pode dirigir." //se falso
)
// - valor inicial é 21, se a variavel age for maior ou ingual a 18 ela recebe dois valores, (?) se for verdadeira e (:) se for falsa, 


console.log(
    age < 18
    ? "Você não pode dirigir."
    : "Você pode dirigir."
)
 
//  se a variavel inicial for menor que 18 ela vai exibir o valor de (?) se for maios vai exibir o valor de (:)