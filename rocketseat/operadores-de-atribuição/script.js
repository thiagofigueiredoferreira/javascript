let value // Cria a variável value sem valor inicial (undefined).

// Operador atribuição
value = 1 // value = 1 define value como 1.
console.log(value) // console.log(value) exibe 1.

// Incremento
value += 2 // value += 2 é igual a value = value + 2.
console.log(value) // Como value era 1, agora vira 3.

//Decremento
value -= 1 // value -= 1 é igual a value = value - 1.
console.log(value) // 3 - 1 resulta em 2.

//Multiplicvar
value *= 3 // value *= 3 é igual a value = value * 3.
console.log(value) // 2 * 3 resulta em 6.

//Divisão
value /= 2 // value /= 2 é igual a value = value / 2.
console.log(value) // 6 / 2 resulta em 3.

