let email = true
let password = false

// AND (E) &&
/*&& (AND): email && password só é true se ambos forem true; caso contrário false. Com os valores do seu código o resultado é false. */
console.log(email && password)

// OR (OU) ||
/*|| (OR): email || password é true se pelo menos um for true; caso ambos sejam false, retorna false. Aqui o resultado é true. */
console.log(email || password)

//NOT (NEGAÇÃO) !
/*! (NOT): !password inverte o valor: true → false, false → true. Aqui !password é true. */
console.log(!password)