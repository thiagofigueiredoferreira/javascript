let number = 10

// INCREMENTO

 // number = number + 1
 number++

 // Incrementa após (por isso não mostra no console)
console.log(number++)
console.log(number)

// Incrementa antes
console.log(++number)

//Decremento
console.log("Decremento após: ", number--)
console.log(number--)
console.log("Decremento antes: ", --number)

//incrementar mais de um (COLOCAR).
number += 10
console.log(number)

//incrementar mais de um (REMOVENDO).
number -= 5
console.log(number)